export type Language = 'en' | 'zh-TW' | 'zh-CN'
export type TranslationKey = keyof typeof translations.en

export function translate(key: TranslationKey, lang: Language, vars?: Record<string, string | number>): string {
  const dict = translations[lang] ?? translations.en
  let str: string = (dict as Record<string, string>)[key] ?? (translations.en as Record<string, string>)[key] ?? key
  if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, String(v)) })
  return str
}
